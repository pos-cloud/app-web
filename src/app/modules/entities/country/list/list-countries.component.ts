  constructor(public _service: CountryService, private _router: Router) {}

  public async emitEvent(event) {
    this.redirect(event.op, event.obj);
  }

  public async redirect(op: string, obj: any) {
    switch (op) {
      case 'view':
        this._router.navigateByUrl('entities/countries/view/' + obj._id);
        break;
      case 'update':
        this._router.navigateByUrl('entities/countries/update/' + obj._id);
        break;
      case 'delete':
        this._router.navigateByUrl('entities/countries/delete/' + obj._id);
        break;
      case 'add':
        this._router.navigateByUrl('entities/countries/add');
        break;
    }
  }
